export function getLocalItem(item, lang) {
  if (!item) return null;
  if (lang === 'pt') return item; // default is Portuguese
  
  const trans = itemTranslations.en?.[item.id] || {};
  
  // Helper to translate nationality if not explicitly provided
  const translateNationality = (nat) => {
    return nationalityMap[nat] || nat;
  };

  // Helper to translate tier if not explicitly provided
  const translateTier = (t) => {
    if (t === 'Lendário') return 'Legendary';
    if (t === 'Ouro') return 'Gold';
    if (t === 'Prata') return 'Silver';
    if (t === 'Bronze') return 'Bronze';
    return t;
  };

  // Helper to translate era if not explicitly provided
  const translateEra = (e) => {
    if (!e) return '';
    return e
      .replace('Clássica', 'Classic')
      .replace('Transição', 'Transition')
      .replace('Moderna', 'Modern');
  };

  return {
    ...item,
    description: trans.description || item.description,
    nationality: translateNationality(trans.nationality || item.nationality),
    tier: translateTier(trans.tier || item.tier),
    era: translateEra(trans.era || item.era)
  };
}

export function getLocalTrack(track, lang) {
  if (!track) return null;
  if (lang === 'pt') return track;
  return {
    ...track,
    country: nationalityMap[track.country] || track.country,
    description: trackTranslationsEn[track.id] || track.description
  };
}
